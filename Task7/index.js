let myAge = 20; // my age
let earlyYears = 2; // early years
earlyYears *= 10.5;
let laterYears = myAge - 2; // later years
laterYears *= 4;
console.log(earlyYears, laterYears);
let myAgeInDogYears = earlyYears + laterYears; // my age in dog years
let myName = "Ram".toLowerCase(); // my name in lower case
console.log(
  `My name is ${myName}. I am ${myAgeInDogYears} years old in dog years.`
); // printing my name and age in dog years to console.
